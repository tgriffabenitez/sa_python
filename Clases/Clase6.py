### Modulos y paquetes
# en python puedo importar un .py con la palabra <import> (siempre y cuando este en la misma carpeta/path)

# import archivo

# luego, puedo usar todoas las funciones usando: archivo.operacion()
# de esta forma importo todas las funciones. Si solo se que voy a usar unas pocas, puedo
# importar de la siguiente forma

# from operaciones import sumar, restar
# from operaciones import *

# hay que tener en cuenta si hay varias funciones con el mismo nombre, es posible que se "pisen"
# es por eso que lo mejor es usar archivo.operacion() ya que especificas la libreria de la funcion
# que queres usar